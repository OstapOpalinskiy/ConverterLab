package com.opalinskiy.ostap.converterlab.models.organisation;


public class Currencies {

    private EUR EUR;
    private RUB RUB;
    private USD USD;

    public EUR getEur() {
        return EUR;
    }

    public void setEur(EUR eur) {
        this.EUR = eur;
    }

    public RUB getRUB() {
        return RUB;
    }

    public void setRUB(RUB RUB) {
        this.RUB = RUB;
    }

    public USD getUSD() {
        return USD;
    }

    public void setUSD(USD USD) {
        this.USD = USD;
    }
}
